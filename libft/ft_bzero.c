#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	int		i;
	char	*bnew;

	i = 0;
	bnew = (char *)s;
	while (i < (int)n)
	{
		bnew[i] = '\0';
		i++;
	}
}
