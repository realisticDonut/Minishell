#include "libft.h"

//added _ for get_var
int	ft_isalnum(int c)
{
	if ((c >= 'a' && c <= 'z')
		|| (c >= 'A' && c <= 'Z')
		|| (c >= '0' && c <= '9')
		|| (c == '_')
		|| (c == '?'))
	{
		return (1);
	}
	return (0);
}
